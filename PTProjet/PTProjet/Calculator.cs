namespace PTProjet
{
    public class Calculator
    {
        public static float Add(float x, float y)
        {
            return x + y;
        }

        public static float Substract(float x, float y)
        {
            return x - y;
        }

        public static float Multiply(float x, float y)
        {
            return x * y;
        }

        public static float Divide(float x, float y)
        {
            return x / y;
        }
    };
}
